/**
 * A random number generator
 */
private RandomNumGenerator numGen;

/**
 * A random suit generator
 */
public RandomSuitGenerator suitGen;

/**
 * Index to suits in generate1Card()
 */
public int index = 0;


//---------CONSTRUCTORS--------------//

/**
 * No argument constructor
 */
public HandGenerator()
{
	//Initialise random number and card suit generators
	numGen = new RandomNumGenerator(13);
	suitGen = new RandomSuitGenerator();
}

