/**
 * Generate 1 card
 */
public String generate1Card()
{
	//The hand
	String theHand = "";

	//Generate 1 random number
	int number = numGen.nextNumber();
	//Generate a random suit
	String suit = suitGen.suits[index];
	index++;

	String value = parseHandValue(number);
	theHand = theHand + value;
	theHand = theHand + suit;

	//Return
	return theHand;
}


/**
 * Generates a two card hand
 */
public String generate2CardHand()
{

	//Initialise random number and card suit generators
	numGen = new RandomNumGenerator(13);
	suitGen = new RandomSuitGenerator();

	//All each hand will be made up of is two numbers and two suits

	//The hand
	String theHand = "";

	//Generate 2 random numbers
    int first = numGen.nextNumber();
    int second = numGen.nextNumber();

    //Generate 2 random suits
    int suitIndex = 0;
    String firstSuit = suitGen.suits[suitIndex];
    suitIndex++;
    String secondSuit = suitGen.suits[suitIndex];

    //Put all together to make a two card hand

	//Convert the first hand value to a String and add suit
	String value1 = parseHandValue(first);
	theHand = theHand + value1;
	theHand = theHand + firstSuit;

	//Add Space
	String space = " ";
	theHand = theHand + space;

	//Second value
	String value2 = parseHandValue(second);
	theHand = theHand + value2;
	theHand = theHand + secondSuit;

    //Return hand
	return theHand;

}


/**
 * Generates a five card hand
 */
public String generate5CardHand()
{
	//Initialise random number and card suit generators
	numGen = new RandomNumGenerator(13);
	suitGen = new RandomSuitGenerator();

	//The five card hand
	String theHand = "";

	//Five random face values
	int first = numGen.nextNumber();
	int second = numGen.nextNumber();
	int third = numGen.nextNumber();
	int fourth = numGen.nextNumber();
	int fifth = numGen.nextNumber();

	//Five random suits
	int suitIndex = 0;
	String firstSuit = suitGen.suits[suitIndex];
    suitIndex++;
    String secondSuit = suitGen.suits[suitIndex];
    suitIndex++;
    String thirdSuit = suitGen.suits[suitIndex];
    suitIndex++;
    String fourthSuit = suitGen.suits[suitIndex];
    suitIndex++;
    String fifthSuit = suitGen.suits[suitIndex];
    suitIndex++;

	//Put all together to get the five card hand

	String space = " ";

	//Convert each value to a string, add suit, and add to hand

	//First card
	String value1 = parseHandValue(first);
	theHand = theHand + value1;
	theHand = theHand + firstSuit;
	theHand = theHand + space;

	//Second card
	String value2 = parseHandValue(second);
	theHand = theHand + value2;
	theHand = theHand + secondSuit;
	theHand = theHand + space;

	//Third card
	String value3 = parseHandValue(third);
	theHand = theHand + value3;
	theHand = theHand + thirdSuit;
	theHand = theHand + space;

	//Fourth card
	String value4 = parseHandValue(fourth);
	theHand = theHand + value4;
	theHand = theHand + fourthSuit;
	theHand = theHand + space;

	//Final card
	String value5 = parseHandValue(fifth);
	theHand = theHand + value5;
	theHand = theHand + fifthSuit;

	//Return the hand
	return theHand;

}

//-------INSTANCE METHODS------------//

